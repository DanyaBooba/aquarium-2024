function ButtonLeftBar(i) {
	if (i == 0) {
		window.open("/feed/", "_self");
	} else if (i == 1) {
		window.open("/person/", "_self");
	} else if (i == 2) {
		window.open("/notifications/", "_self");
	} else if (i == 3) {
		window.open("/search/", "_self");
	} else if (i == 4) {
		window.open("/settings/", "_self");
	}
}
